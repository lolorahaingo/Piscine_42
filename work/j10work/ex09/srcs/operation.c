#include "ft.h"

int		sum(int i, int j)
{
	return (i + j);
}

int		substraction(int i, int j)
{
	return (i - j);
}

int		multiplication(int i, int j)
{
	return (i * j);
}

int		quotient(int i, int j)
{
	return (i / j);
}

int		modulo(int i, int j)
{
	return (i % j);
}
