int		ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int		ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && (i != ft_strlen(s1) || i != ft_strlen(s2)))
		i++;
	return (s1[i] - s2[i]);
}
