#include <stdio.h>

int ft_sqrt(int nb)
{
    int i;
    
    i = 0;
    while (i++ - 1 <= nb && i <= 46351)
    {
        if ((i * i) == nb)
            return (i);
    }
    return (0);
}

int main (void)
{
	printf("%i", ft_sqrt(-23));
	return (0);
}
