#include "ft.h"

int		ft_add(int i, int j)
{
	return (i + j);
}

int		ft_sub(int i, int j)
{
	return (i - j);
}

int		ft_mul(int i, int j)
{
	return (i * j);
}

int		ft_div(int i, int j)
{
	return (i / j);
}

int		ft_mod(int i, int j)
{
	return (i % j);
}
