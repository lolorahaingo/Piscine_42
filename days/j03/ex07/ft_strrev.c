char	*ft_strrev(char *str)
{
	int		i;
	int		a;
	char	tmp;

	i = 0;
	while (str[i] != '\0')
		i++;
	i = i - 1;
	a = 0;
	while (i > a)
	{
		tmp = str[i];
		str[i] = str[a];
		str[a] = tmp;
		i--;
		a++;
	}
	return (str);
}
